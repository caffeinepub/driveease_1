export function lookupPincode(
  pincode: string,
): { city: string; state: string } | null {
  const p = pincode.trim();
  if (p.length < 3) return null;
  // Try exact 6-digit match first
  if (p.length === 6 && PINCODE_PREFIX_MAP[p]) return PINCODE_PREFIX_MAP[p];
  // Try 3-digit prefix
  const prefix3 = p.slice(0, 3);
  if (PINCODE_PREFIX_MAP[prefix3]) return PINCODE_PREFIX_MAP[prefix3];
  return null;
}

